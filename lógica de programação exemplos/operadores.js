//Comparação

console.log("maior q", ">", "4>2");// true
console.log("menor q", "<", "4<2");//falese
console.log("maior ou igual q", ">=", "4>=2");//true
console.log("menor ou igual q", "<=", "4<=2");//false
console.log("igualdade", "==", "4==2");//false
console.log("igualdade restrita", "===", "2===2");//true
console.log("diferente", "!=", "4!=2");//true

//lógicos

console.log("E","&&",1>2 && 2>1);// so retorna true se tudo for vdd
